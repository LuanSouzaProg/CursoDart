void switchAula(){
  String linguagem = "Dart";

  switch(linguagem){
    case "Dart":
      print("Melhor linguagem");
      break;
    case "C":
      print("Linguagem complicada");
      break;
    case "Swift":
      print("Linguagem complicada também");
      break;
    default:
      print("Linguagem desconhecida");
  }

  int number = 10;

  switch(number){
    case 10:
      print("Seu número é 10");
      break;
    case 20:
      print("Seu número é 20");
      break;
  }
}